// math_library.h
#ifndef MATH_LIBRARY_H
#define MATH_LIBRARY_H

/**
 * Cộng hai số nguyên
 * @param a Số thứ nhất
 * @param b Số thứ hai
 * @return Tổng của hai số
 */
int add(int a, int b);

/**
 * Trừ số thứ hai từ số thứ nhất
 * @param a Số thứ nhất
 * @param b Số thứ hai
 * @return Hiệu của hai số
 */
int subtract(int a, int b);

/**
 * Nhân hai số nguyên
 * @param a Số thứ nhất
 * @param b Số thứ hai
 * @return Tích của hai số
 */
int multiply(int a, int b);

/**
 * Chia số thứ nhất cho số thứ hai
 * @param a Số thứ nhất
 * @param b Số thứ hai
 * @return Thương của hai số
 */
int divide(int a, int b);

#endif // MATH_LIBRARY_H
